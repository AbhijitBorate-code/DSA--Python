def Bubble_sorting(L):
    for i in range(len(L)):  # this for loop use for how many time loop iterble
        for j in range(len(L)-1):  # this low used for comparison
            if L[j] > L[j + 1]:
                L[j], L[j + 1] = L[j + 1], L[j]
    return L


